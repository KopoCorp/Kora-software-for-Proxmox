def colorize_text(input_text):
    """ GÃ¨re la coloration de kobot"""
    color_mappings = {
        r"#+": ("[#6C6392]", "[/#6C6392]"),
        r"\\.": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"@": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"<.*?>": ("[#FFFFFF]", "[/#FFFFFF]"),
        r".+": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"^": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"o": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"O": ("[#FFFFFF]", "[/#FFFFFF]"),
        r"/(?=[^#])": ("[#FF012B]", "[/#FF012B]"),
    }

    output_text = input_text
    for pattern, (start_tag, end_tag) in color_mappings.items():
        output_text = re.sub(
            pattern,
            lambda match: f"{start_tag}{match.group(0)}{end_tag}",
            output_text
        )

    return output_text

